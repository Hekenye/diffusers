class TextualInversionDataset(Dataset):
    def __init__(
        self,
        data_root,
        tokenizer_one,
        tokenizer_two,
        learnable_property="style",  # [object, style]
        size=1024,
        repeats=100,
        interpolation="bicubic",
        random_flip=True,
        flip_p=0.5,
        set="train",
        content=None,
        placeholder_token="*",
        center_crop=False,
    ):
        self.data_root = data_root
        self.tokenizer_one = tokenizer_one
        self.tokenizer_two = tokenizer_two
        self.learnable_property = learnable_property
        self.size = size
        self.content = content
        self.placeholder_token = placeholder_token
        self.center_crop = center_crop
        self.random_flip = random_flip
        self.flip_p = flip_p

        self.image_paths = [os.path.join(self.data_root, file_path) for file_path in os.listdir(self.data_root)]

        self.num_images = len(self.image_paths)
        self._length = self.num_images

        if set == "train":
            self._length = self.num_images * repeats

        self.interpolation = {
            "linear": PIL_INTERPOLATION["linear"],
            "bilinear": PIL_INTERPOLATION["bilinear"],
            "bicubic": PIL_INTERPOLATION["bicubic"],
            "lanczos": PIL_INTERPOLATION["lanczos"],
        }[interpolation]

        if learnable_property == "style":
            if content is None:
                self.templates = imagenet_style_templates_small
            else:
                self.templates = imagenet_style_templates_small_with_object
        else:
            assert content is None, print("if learnable_property == 'style', then we can not set content")
            self.templates = imagenet_templates_small
        
        # image transforms
        self.train_resize = transforms.Resize(size, interpolation=transforms.InterpolationMode.BILINEAR)
        self.train_crop = transforms.CenterCrop(size) if center_crop else transforms.RandomCrop(size)
        self.train_flip = transforms.RandomHorizontalFlip(p=1.0)
        self.train_transforms = transforms.Compose(
            [
                transforms.ToTensor(),
                transforms.Normalize([0.5], [0.5]),
            ]
        )

    def __len__(self):
        return self._length

    def __getitem__(self, i):
        example = {}
        placeholder_string = self.placeholder_token
        content = self.content
        if not content is None:
            text = random.choice(self.templates).format(content, placeholder_string)
        else:
            text = random.choice(self.templates).format(placeholder_string)

        example["input_ids_one"] = self.tokenizer_one(
            text,
            padding="max_length",
            truncation=True,
            max_length=self.tokenizer_one.model_max_length,
            return_tensors="pt",
        ).input_ids[0]
        example["input_ids_two"] = self.tokenizer_two(
            text,
            padding="max_length",
            truncation=True,
            max_length=self.tokenizer_two.model_max_length,
            return_tensors="pt",
        ).input_ids[0]

        image = Image.open(self.image_paths[i % self.num_images]).convert("RGB")
        original_size = (image.height, image.width)
        image = self.train_resize(image)
        if self.center_crop:
            y1 = max(0, int(round((image.height - self.size) / 2.0)))
            x1 = max(0, int(round((image.width - self.size) / 2.0)))
            image = self.train_crop(image)
        else:
            y1, x1, h, w = self.train_crop.get_params(image, (self.size, self.size))
            image = transforms.functional.crop(image, y1, x1, h, w)
        if self.random_flip and random.random() < 0.5:
            # flip
            x1 = image.width - x1
            image = self.train_flip(image)
        crop_top_left = (y1, x1)
        image = self.train_transforms(image)
        example["original_sizes"] = original_size
        example["crop_top_lefts"] = crop_top_left
        example["pixel_values"] = image
        return example


